package com.mycompany.nbastats.model.entity.franchise;

import javax.persistence.Entity;
import javax.persistence.Id;

/**
 *
 * @author cristobal
 */
@Entity
public class Franchise {
    
    @Id
    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}
